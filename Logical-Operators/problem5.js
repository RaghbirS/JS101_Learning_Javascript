let weekDay = "Fri";
switch (weekDay) {
  case "Sun": console.log("Sunday");
    break
  case "Mon": console.log("Tuesday");
    break
  case "Tue": console.log("Wednesday");
    break
  case "Wed": console.log("Thursday");
    break
  case "Thu": console.log("Friday");
    break
  case "Fri": console.log("Saturday");
    break
  case "Sat": console.log("Monday");


}